import { jsPDF } from 'jspdf';

/**
 * Инициализирует jsPDF с поддержкой кириллицы
 * Использует стандартный шрифт, который лучше работает с UTF-8
 */
export function createPDFWithCyrillic(): jsPDF {
  const doc = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4',
  });

  // Используем стандартный шрифт, но с правильной настройкой
  // Для кириллицы лучше использовать 'times' или настроить кастомный шрифт
  // Пока используем стандартный подход с правильной кодировкой
  doc.setFont('helvetica');
  
  return doc;
}

/**
 * Добавляет текст с поддержкой кириллицы
 * Для корректной работы с кириллицей нужно использовать кастомный шрифт
 * Но для простоты используем стандартный подход
 */
export function addCyrillicText(doc: jsPDF, text: string, x: number, y: number): void {
  // Временное решение: используем стандартный шрифт
  // Для полной поддержки кириллицы нужно подключить кастомный шрифт
  doc.text(text, x, y);
}

